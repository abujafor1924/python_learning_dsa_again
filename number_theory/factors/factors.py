# ------------------------------
# ইউজারের ইনপুট সংখ্যা নেয়া
# ------------------------------
num = int(input("একটি সংখ্যা লিখুন: "))

# ------------------------------
# গুণনীয়ক বের করার জন্য লিস্ট
# ------------------------------
factors = []

print(f"\n{num} এর গুণনীয়ক চেক করা হচ্ছে...")
for i in range(1, num + 1):
    if num % i == 0:
        print(f"{i} দ্বারা ভাগ যায় → এটি গুণনীয়ক")
        factors.append(i)

# ------------------------------
# সব গুণনীয়ক দেখানো
# ------------------------------
print(f"\n{num} এর সব গুণনীয়ক: {factors}")
print(f"মোট {num} এর গুণনীয়ক সংখ্যা:", len(factors))
